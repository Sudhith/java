public class Arr_Q4 
{
    public static void main(String[] a)
    {
        int[] arr = {1, 2, 3, 4, 5};
        int sum = 0;
        for(int i = 0; i < arr.length; i++)
        {
            sum += arr[i];
        }
        double average = (double) sum / arr.length;
        System.out.println("Sum: " + sum);
        System.out.println("Average: " + average);
    }
    
}
